#4. Basic Operations

#Arithmetic Operations

'''Addition (+): Adding two numbers together.
Subtraction (-): Subtracting one number from another.
Multiplication (*): Multiplying two numbers.
Division (/): Dividing one number by another.
Exponentiation ()**: Raising a number to the power of another.'''

#Assignment Operators

'''Assignment (=): Assigning a value to a variable.
Addition Assignment (+=): Adding a value to a variable and assigning the result.
Subtraction Assignment (-=): Subtracting a value from a variable and assigning the result.
Multiplication Assignment (*=): Multiplying a variable by a value and assigning the result.
Division Assignment (/=): Dividing a variable by a value and assigning the result.'''

#Comparison Operators

'''Equal to (==): Checking if two values are equal.
Not Equal to (!=): Checking if two values are not equal.
Greater than (>) and Greater than or Equal to (>=): Comparing two values to check if one is greater than the other.
Less than (<) and Less than or Equal to (<=): Comparing two values to check if one is less than the other.'''

#Logical Operators

'''AND (and): Checking if both conditions are true.
OR (or): Checking if at least one condition is true.
NOT (not): Negating a condition.'''

